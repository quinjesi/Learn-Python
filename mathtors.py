#Math Operators
print(10+3) #Addition
print(10-3) #Subtraction
print(10*3) #	Multiplication
print(10/3) #Division
print(10//3) #Divisoion but returns only the whole number
print(10%3) #Returns the remainder of a division
print(10**3) #Exponetial(Power)


#Incrementing a number
x = 10
